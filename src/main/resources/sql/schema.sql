  id BIGINT PRIMARY KEY NOT NULL AUTO_INCREMENT,
  patient_id BIGINT REFERENCES patients(id),
  date_time TIMESTAMP NOT NULL,
  status VARCHAR(50) NOT NULL -- e.g., "SCHEDULED", "COMPLETED"
);

